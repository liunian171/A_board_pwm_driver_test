/**
 * ============================================================================
 *  UART 驱动 — 策略层（跨平台通用）
 * ============================================================================
 *
 *  本文件包含 UART 驱动的"策略函数"——它们通过 hUART->ops 调用底层
 *  硬件操作，自身只做参数转发，不直接碰任何 HAL 库或寄存器。
 *
 *  ▸ 设计分界线 ◂
 *  本文件中的函数（策略层）与 uart_platform_ops.c（平台实现层）的界限：
 *
 *    ┌─ 策略层（本文件）─────────────┬─ 平台实现层（ops）──────────┐
 *    │  uart_send()                 │  send() / receive()         │
 *    │  uart_receive()              │  receive_IT()               │
 *    │  uart_receive_IT()           │                             │
 *    │  uart_flush_rx()             │                             │
 *    ├──────────────────────────────┼─────────────────────────────┤
 *    │  参数转发（零逻辑）            │  原子操作（HAL 封装）         │
 *    │  跨平台通用                   │  每款芯片一套                │
 *    │  换平台零修改                 │  换平台整个文件替换           │
 *    └──────────────────────────────┴─────────────────────────────┘
 *
 *  参考: pwm.c 的同架构设计思路, UART_Serial_Design.md 第三章
 * ============================================================================
 */

#include "uart.h"
#include <stdint.h>


/*==============================================================================
 *  发送
 *==============================================================================*/

/**
 * @brief  阻塞发送 len 字节
 * @param  hUART  UART 句柄
 * @param  data   待发送数据缓冲区
 * @param  len    发送字节数
 * @return 0 成功, -1 失败
 */
int8_t uart_send(UART_Handle *hUART, const uint8_t *data, uint16_t len)
{
    return hUART->ops->send(hUART->huart, data, len);
}


/*==============================================================================
 *  接收（阻塞 — 仅调试用，正常接收走中断版本）
 *==============================================================================*/

/**
 * @brief  阻塞接收 len 字节
 * @param  hUART  UART 句柄
 * @param  data   接收数据缓冲区
 * @param  len    接收字节数
 * @return 0 成功, -1 失败
 */
int8_t uart_receive(UART_Handle *hUART, uint8_t *data, uint16_t len)
{
    return hUART->ops->receive(hUART->huart, data, len);
}


/*==============================================================================
 *  中断接收
 *==============================================================================*/

/**
 * @brief  启动中断接收
 *
 *         每收到 1 字节硬件自动触发 UART7_IRQHandler
 *         → HAL_UART_IRQHandler → HAL_UART_RxCpltCallback
 *         回调中必须重新调本函数使能下一个字节，否则只收到一个。
 *
 * @param  hUART   UART 句柄
 * @param  p_byte  存放收到字节的地址（通常指向 hUART->rx_byte）
 * @return 0 成功, -1 失败
 */
int8_t uart_receive_IT(UART_Handle *hUART, uint8_t *p_byte)
{
    return hUART->ops->receive_IT(hUART->huart, p_byte, 1);
}


